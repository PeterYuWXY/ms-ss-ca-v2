export function t(lang: Lang | string, key: string, vars?: Record<string, string | number>): string {
  const l = (messages[lang as Lang] ? lang as Lang : 'en');
  let msg = messages[l][key] ?? messages.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      msg = msg.replaceAll(`{${k}}`, String(v));
    }
  }
  return msg;
}
